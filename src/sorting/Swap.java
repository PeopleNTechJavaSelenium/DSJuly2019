package sorting;

public class Swap {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 12;
        int temp;
        System.out.println("Num1: " + num1);
        System.out.println("Num2: " + num2);
        temp = num1;
        num1 = num2;
        num2 = temp;
        System.out.println("Num1: " + num1);
        System.out.println("Num2: " + num2);
    }
}
